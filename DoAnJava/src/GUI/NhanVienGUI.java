    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // Lấy thông tin từ các trường nhập liệu
        String TenNV = txtTenNV.getText();
        String GioiTinh = txtGioiTinh.getText();
        String DiaChi = txtDiaChi.getText();
        String SDT = txtSDT.getText();
        String PhanQuyen = "Nhân viên";
        char[] chuoiMatKhau = txtMatKhau.getPassword();
        
        String MatKhau = new String(chuoiMatKhau);
        int MaNV = Integer.parseInt(txtMaNV.getText());

        // Tạo một đối tượng nhân viên mới với thông tin cập nhật
        NhanVien nv = new NhanVien(MaNV, TenNV, GioiTinh, DiaChi, SDT, PhanQuyen, MatKhau);

        // Hiển thị hộp thoại xác nhận
        int choice = JOptionPane.showConfirmDialog(null, "Bạn có chắc chắn muốn cập nhật thông tin nhân viên này?", "Xác nhận cập nhật", JOptionPane.YES_NO_OPTION);

        // Nếu người dùng chọn "Yes" (có)
        if (choice == JOptionPane.YES_OPTION) {
            // Thực hiện cập nhật thông tin nhân viên
            if (NhanVienDAO.updateNhanVien(nv)) {
                // Hiển thị thông báo thành công
                JOptionPane.showMessageDialog(null, "Cập nhật thông tin nhân viên thành công!");
            } else {
                // Hiển thị thông báo thất bại
                JOptionPane.showMessageDialog(null, "Cập nhật thông tin nhân viên thất bại!");
            }

            // Sau khi cập nhật xong, cập nhật lại danh sách nhân viên và xóa dữ liệu trên các trường nhập liệu
            hienThiDanhSachNhanVien();
            clearData();
        }

    }//GEN-LAST:event_btnSuaActionPerformed

    private void btnTimActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimActionPerformed
        // TODO add your handling code here:
        String tim = txtTim.getText();
        if (tim != "") {
            ArrayList<NhanVien> dsNhanViens = new ArrayList<>();
            NhanVien v = new NhanVien(0, tim, tim, tim, tim, tim, tim);
            dsNhanViens = NhanVienDAO.searchNhanVien(v);
            DefaultTableModel dtm = new DefaultTableModel();
            String[] tieuDe = {"Mã nhân viên", "Họ tên", "Giới tính", "Địa chỉ", "Số ĐT"};
            dtm.setColumnIdentifiers(tieuDe);
            for (NhanVien nv : dsNhanViens) {
                Vector<Object> vector = new Vector<>();
                vector.add(nv.getMaNV());
                vector.add(nv.getTenNV());
                vector.add(nv.getGioiTinh());
                vector.add(nv.getDiaChi());
                vector.add(nv.getSoDienThoai());
                dtm.addRow(vector);
            }
            tblNhanVien.setModel(dtm);
        }
    }//GEN-LAST:event_btnTimActionPerformed


