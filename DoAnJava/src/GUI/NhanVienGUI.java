    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        int choice = JOptionPane.showConfirmDialog(null, "Bạn có chắc chắn muốn thêm nhân viên này?", "Xác nhận xóa", JOptionPane.YES_NO_OPTION);
        // Nếu người dùng chọn "Yes" (có)
        if (choice == JOptionPane.YES_OPTION) {
            String TenNV = txtTenNV.getText();
            String GioiTinh = txtGioiTinh.getText();
            String DiaChi = txtDiaChi.getText();
            String SDT = txtSDT.getText();
            String PhanQuyen = "Nhân viên";
            String MatKhau = txtMatKhau.getText();

            NhanVien nv = new NhanVien(0, TenNV, GioiTinh, DiaChi, SDT, PhanQuyen, MatKhau);

            if (NhanVienDAO.insertNhanVien(nv)) {
                JOptionPane.showMessageDialog(null, "Thêm nhân viên thành công!");
            } else {
                JOptionPane.showMessageDialog(null, "Thêm nhân viên thất bại!");
            }
            hienThiDanhSachNhanVien();
            clearData();
        }

    }//GEN-LAST:event_btnThemActionPerformed

