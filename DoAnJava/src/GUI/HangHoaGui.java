    private void cboLoaiHHSearchItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cboLoaiHHSearchItemStateChanged
        if (!cboLoaiHHSearch.getSelectedItem().toString().equals("Tất cả")) {
            clearData();
            int MaLoai;
            String TenLoai = cboLoaiHHSearch.getSelectedItem().toString();
            LoaiHH lhh = LoaiHHDAO.timLoaiHHTheoTen(TenLoai);
            MaLoai = lhh.getMaLH();
            ArrayList<HangHoa> dsHangHoas = HangHoaDAO.searchHangHoaTheoLoai(MaLoai);
            DefaultTableModel dtm = new DefaultTableModel();
            String[] tieuDe = {"Mã hàng", "Tên loại hàng", "Tên hàng", "Giá"};
            dtm.setColumnIdentifiers(tieuDe);
            for (HangHoa hh : dsHangHoas) {
                Vector<Object> vector = new Vector<>();
                vector.add(hh.getMaHH());
                vector.add(hh.getMaLH());
                vector.add(hh.getTenHH());
                vector.add(hh.getGiaSP());
                dtm.addRow(vector);
            }
            tblHangHoa.setModel(dtm);
        } else {
            hienThiDanhSachHH();
        }

    }//GEN-LAST:event_cboLoaiHHSearchItemStateChanged


