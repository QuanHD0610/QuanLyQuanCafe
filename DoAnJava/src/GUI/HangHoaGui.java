    private void tblHangHoaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblHangHoaMouseClicked
        // TODO add your handling code here
        int row = tblHangHoa.getSelectedRow();

        edtMaHH.setText(tblHangHoa.getValueAt(row, 0).toString());
        int MaHH = Integer.parseInt(edtMaHH.getText());
        HangHoa hh = HangHoaDAO.searchHangHoaTheoma(MaHH);
        txtTenHH.setText(hh.getTenHH());
        txtGiaHH.setText(String.valueOf(hh.getGiaSP()));

        String TenLoai;
        int MaLoai = Integer.parseInt(tblHangHoa.getValueAt(row, 1).toString());
        LoaiHH lhh = LoaiHHDAO.timLoaiHHTheoMa(Integer.parseInt(tblHangHoa.getValueAt(row, 1).toString()));
        if (lhh != null) {
            TenLoai = lhh.getTenLH();
            for (int i = 0; i < cboLoaiHH.getItemCount(); i++) {
                if (TenLoai.equals(cboLoaiHH.getItemAt(i))) {
                    cboLoaiHH.setSelectedItem(TenLoai);
                    break; // Thoát vòng lặp khi đã tìm thấy và đặt giá trị cho combobox
                }
            }
        }
    }//GEN-LAST:event_tblHangHoaMouseClicked

