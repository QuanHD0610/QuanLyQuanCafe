    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        String TenHH = txtTenHH.getText().trim();
        String TenLoai = cboLoaiHH.getSelectedItem().toString();
        int GiaHH = 0; // Khởi tạo giá trị mặc định
        try {
            GiaHH = Integer.parseInt(txtGiaHH.getText());
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(null, "Giá hàng hóa không hợp lệ!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (TenHH.isEmpty() || TenLoai.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Vui lòng nhập đầy đủ thông tin!", "Cảnh báo", JOptionPane.WARNING_MESSAGE);
            return;
        }

        int choice = JOptionPane.showConfirmDialog(null, "Bạn có chắc chắn muốn thêm?", "Xác nhận thêm", JOptionPane.YES_NO_OPTION);
        // Nếu người dùng chọn "Yes" (có)
        if (choice == JOptionPane.YES_OPTION) {
            int MaLH;
            LoaiHH lhh = LoaiHHDAO.timLoaiHHTheoTen(TenLoai);
            if (lhh == null) {
                JOptionPane.showMessageDialog(null, "Không tìm thấy loại hàng hóa!", "Lỗi", JOptionPane.ERROR_MESSAGE);
                return;
            }
            MaLH = lhh.getMaLH();
            HangHoa hh = new HangHoa(0, MaLH, TenHH, "", GiaHH);

            if (HangHoaDAO.insertHangHoa(hh)) {
                JOptionPane.showMessageDialog(null, "Thêm thành công!");
                hienThiDanhSachHH();
                clearData();
            } else {
                JOptionPane.showMessageDialog(null, "Thêm thất bại!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnThemActionPerformed

