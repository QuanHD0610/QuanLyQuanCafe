    private void cbbTinhTrangBanItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cbbTinhTrangBanItemStateChanged
        // TODO add your handling code here:
        if (!cbbTinhTrangBan.getSelectedItem().toString().equals("Tất cả")) {
            int maBan;
            String tinhTrangBan = cbbTinhTrangBan.getSelectedItem().toString();
            dsBan = BanDAO.searchBanTheoTinhTrang(tinhTrangBan);
            DefaultTableModel dtm = new DefaultTableModel();
            String[] tieuDe = {"Mã Bàn", "Tên bàn", "Trạng thái bàn", "Hóa đơn hiện tại"};
            dtm.setColumnIdentifiers(tieuDe);
            for (Ban ban : dsBan) {
                Vector<Object> vector = new Vector<>();
                vector.add(ban.getMaBan());
                vector.add(ban.getTenBan());
                vector.add(ban.getTrangThai());
                vector.add(ban.getHdHienTai());
                dtm.addRow(vector);
            }
            tblBan.setModel(dtm);
        } else {
            hienThiDanhSachBan();
        }
    }//GEN-LAST:event_cbbTinhTrangBanItemStateChanged

