    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        String TenBan = txtTenBan.getText().trim();

        int choice = JOptionPane.showConfirmDialog(null, "Bạn có chắc chắn muốn thêm?", "Xác nhận thêm", JOptionPane.YES_NO_OPTION);
        // Nếu người dùng chọn "Yes" (có)
        if (choice == JOptionPane.YES_OPTION) {
            int MaBan = 0;
            String tenBan = txtTenBan.getText().toString();
            String trangThai = txtTenBan.getText().toString();
            int hoaDonHT = Integer.parseInt(txtHoaDonHienTai.getText());
            Ban ban = new Ban(MaBan, TenBan,trangThai ,hoaDonHT);

            if (BanDAO.insertBan(ban)) {
                JOptionPane.showMessageDialog(null, "Thêm thành công!");
                hienThiDanhSachBan();
                clearData();
            } else {
                JOptionPane.showMessageDialog(null, "Thêm thất bại!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnThemActionPerformed

