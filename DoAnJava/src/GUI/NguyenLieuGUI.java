    public void hienThiDanhSachNguyenLieu() {
        dsNguyenLieus = NguyenLieuDao.layDanhSachNguyenLieu();
        DefaultTableModel dtm = new DefaultTableModel();
        String[] tieuDe = {"Mã Nguyên Liệu", "Tên Nguyên Liệu", "Đơn vị tính", "Số lượng tồn"};
        dtm.setColumnIdentifiers(tieuDe);
        for (NguyenLieu nl : dsNguyenLieus) {
            Vector vec = new Vector();
            vec.add(nl.getMaNL());
            vec.add(nl.getTenNL());
            vec.add(nl.getDonViTinh());
            vec.add(nl.getSoLuongTon());
            dtm.addRow(vec);
        }
        tblNguyenLieu.setModel(dtm);
    }

