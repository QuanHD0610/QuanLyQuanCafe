    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        int choice = JOptionPane.showConfirmDialog(null, "Bạn có chắc chắn muốn thêm nguyên liệu này?", "Xác nhận thêm", JOptionPane.YES_NO_OPTION);
        if (choice == JOptionPane.YES_OPTION) {
            String tenNL = txtTenNL.getText();
            String dvtString = txtDVT.getText();
            int slt = Integer.parseInt(txtSLTon.getText());
            NguyenLieu nguyenLieu = new NguyenLieu(0, tenNL, dvtString, slt);
            if (NguyenLieuDao.insertNguyenLieu(nguyenLieu)) {
                JOptionPane.showMessageDialog(null, "Thêm nguyên liệu thành công!");
            } else {
                JOptionPane.showMessageDialog(null, "Thêm nguyên liệu thất bại!");
            }
        }
        hienThiDanhSachNguyenLieu();

    }//GEN-LAST:event_btnThemActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
            // Lấy mã nhân viên từ trường txtMaNV
        int MaNV = Integer.parseInt(txtMaNL.getText());

        // Tạo một đối tượng nhân viên mới với mã nhân viên được lấy
        NguyenLieu nv = new NguyenLieu(MaNV, "", "", 0);

        // Hiển thị hộp thoại xác nhận
        int choice = JOptionPane.showConfirmDialog(null, "Bạn có chắc chắn muốn xóa nhân viên này?", "Xác nhận xóa", JOptionPane.YES_NO_OPTION);

        // Nếu người dùng chọn "Yes" (có)
        if (choice == JOptionPane.YES_OPTION) {
            // Thực hiện xóa nhân viên
            if (NguyenLieuDao.deleteHangHoa(nv)) {
                // Hiển thị thông báo thành công
                JOptionPane.showMessageDialog(null, "Xóa nhân viên thành công!");
            } else {
                // Hiển thị thông báo thất bại
                JOptionPane.showMessageDialog(null, "Xóa nhân viên thất bại!");
            }

            // Sau khi xóa xong, cập nhật lại danh sách nhân viên
            hienThiDanhSachNguyenLieu();
        }
    }//GEN-LAST:event_btnXoaActionPerformed
